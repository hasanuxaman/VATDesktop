                    foreach (DataRow item in IssueResult.Rows)
                   {
                        DataGridViewRow NewRow = new DataGridViewRow();
                        dgvIssueHistory.Rows.Add(NewRow);

                        dgvIssueHistory.Rows[j].Cells["VAT7No"].Value = item["VAT7No"].ToString();// IssueFields[0].ToString();
                        dgvIssueHistory.Rows[j].Cells["VAT7Date"].Value = item["VAT7Date"].ToString();// IssueFields[1].ToString();

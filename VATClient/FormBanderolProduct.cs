        private int ErrorReturn()
        {
            #region try
            try
            {
                if (txtItemNo.Text == "")
                {
                    MessageBox.Show("Please select a Item", this.Text);
                    return 1;
                }
                else
                {
                    ItemNo = txtItemNo.Text;
                }

                if (txtBandeId.Text.Trim() == "")
                {
                    MessageBox.Show("Please select Banderol .");
                    txtBanderol.Focus();
                    return 1;
                }
                else
                {
                    BanderolId = txtBandeId.Text;
                }
                if (txtPackId.Text.Trim() == "")
                {
                    MessageBox.Show("Please select Packageing Nature.");
                    txtPackId.Focus();
                    return 1;
                }
                else
                {
                    PackagingId = txtPackId.Text;
                }

                if (txtBUsedQty.Text.Trim() == "" || txtBUsedQty.Text.Trim() == "0.00")
                {
                    MessageBox.Show("Please enter B Used Quantity.");
                    txtBUsedQty.Focus();
                    return 1;
                }
                else
                {
                    BUsedQty = txtBUsedQty.Text;
                }
                if (txtWastQty.Text.Trim() == "" || txtWastQty.Text.Trim() == "0.00")
                {
                    MessageBox.Show("Please enter Wastage Quantity.");
                    txtWastQty.Focus();
                    return 1;
                }
                else
                {
                    WastageQty = (Convert.ToDecimal(txtWastQty.Text) / 100).ToString();
                }
                if (txtOpeningQty.Text.Trim() == "")
                {
                    MessageBox.Show("Please enter Quantity.");
                    txtOpeningQty.Focus();
                    return 1;
                }
                else
                {
                    OpeningQty = Convert.ToDecimal(txtOpeningQty.Text);
                }
                OpeningDate = dtpOpeningDate.Value.ToString("yyyy-MMM-dd HH:mm:ss");
            }
            #endregion

            #region catch
            catch (ArgumentNullException ex)
            {
                string err = ex.Message.ToString();
                string[] error = err.Split(FieldDelimeter.ToCharArray());
